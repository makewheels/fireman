package com.eg.fireman.bean.result;

public class WrongQuestionInfo {

	private long total;
	private long single;
	private long multiple;
	private long check;

	public long getTotal() {
		return total;
	}

	public void setTotal(long total) {
		this.total = total;
	}

	public long getSingle() {
		return single;
	}

	public void setSingle(long single) {
		this.single = single;
	}

	public long getMultiple() {
		return multiple;
	}

	public void setMultiple(long multiple) {
		this.multiple = multiple;
	}

	public long getCheck() {
		return check;
	}

	public void setCheck(long check) {
		this.check = check;
	}

}
